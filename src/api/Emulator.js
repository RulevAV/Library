export let GetListBook = (count, page, sort, SortAuthor, SortTitle) => {

    let mass = [];
    let Select = [];
    Select = [...ListBook].filter((awrd) => awrd.author.toLowerCase().indexOf(SortAuthor.toLowerCase()) > -1);
    Select = Select.filter((awrd) => awrd.title.toLowerCase().indexOf(SortTitle.toLowerCase()) > -1);
    switch (sort) {
        case 0:
            mass = Select.sort((a, b) => {
                if (a.author > b.author)
                    return 1
                else return -1;
            });
            break;
        case 1:
            mass = Select.sort((a, b) => {
                if (a.title > b.title)
                    return 1
                else return -1;
            });
            break;
        case 2:
            mass = Select.sort((a, b) => {
                return a.rating - b.rating;
            });
            break;
        default:
            return [];
    }
    const CountBooks = mass.length;

    const PartListBook = mass.slice((count - 1) * page, count * page);
    return {PartListBook, CountBooks};

}

